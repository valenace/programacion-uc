#include <cstdio>

int main() {
    // división entera: 10 dividido entre 4 da como resultado 2.
    printf("10/4 = %d\n", 10/4);

    // módulo: el residuo de dividir 5 entre 7 es 5.
    printf("5%%7 = %d\n", 5%7);

    // división de números de punto flotante: 10.5 dividido entre 3.0 da como resultado 3.5.
    printf("10.5/3.0 = %.2f\n", 10.5/3.0);

    // división entera: 10 dividido entre 3 da como resultado 3.
    printf("10/3 = %d\n", 10/3);

    // módulo: el residuo de dividir 20 entre 3 es 2.
    printf("20%%3 = %d\n", 20%3);

    // potencia: 4 al cuadrado (4*4) es 16.
    printf("4^2 = %d\n", 4*4);

    // módulo: el residuo de dividir 10 entre 3 es 1.
    printf("10%%3 = %d\n", 10%3);

    // división entera: 30 dividido entre 30 da como resultado 1.
    printf("30/30 = %d\n", 30/30);

    return 0;
}


